def add(a, b):
    """
    Returns the sum of two numbers.
    Args:
        a (int, float): The first number.
        b (int, float): The second number.
    Returns:
        int, float: The sum of a and b.
    """
    return a + b

def subtract(a, b):
    """ 
    Returns the difference of two numbers.
    Args:
        a (int, float): The first number.
        b (int, float): The second number.
    Returns:
        int, float: The difference of a and b.
    """
    return a - b